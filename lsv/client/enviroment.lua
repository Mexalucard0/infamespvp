local _weather = nil

local function RequestIplAsync(iplName)
	RequestIpl(iplName)
	Citizen.Wait(0)
end

local function RemoveIplAsync(iplName)
	RemoveIpl(iplName)
	Citizen.Wait(0)
end

RegisterNetEvent('lsv:updateWeather')
AddEventHandler('lsv:updateWeather', function(weather, applyNow)
	if _weather == weather then
		return
	end

	_weather = weather

	if not applyNow then
		SetWeatherTypeOverTime(_weather, 30.0)
		Citizen.Wait(30000)
	end

	SetWeatherTypeNowPersist(_weather)
end)

Citizen.CreateThread(function()
	World.AddVehicleHandler(function(vehicle)
		if NetworkGetEntityIsNetworked(vehicle) and GetVehicleEngineHealth(vehicle) <= -4000 then
			Network.DeleteVehicle(VehToNet(vehicle), 10000)
		end
	end)
end)

Citizen.CreateThread(function()
	table.iforeach(Settings.world.vehicleGenerators, function(generatorData)
		Streaming.RequestModelAsync(generatorData.modelHash)

		local generator = CreateScriptVehicleGenerator(generatorData.x, generatorData.y, generatorData.z, generatorData.heading, 5.0, 3.0, generatorData.modelHash, -1, -1, -1, -1, true, false, false, true, true, -1)
		SetScriptVehicleGenerator(generator, true)

		Map.CreatePlaceBlip(Blip.MILITARY_VEHICLE_WORLD_SPAWN, generatorData.x, generatorData.y, generatorData.z, 'Spawn de Vehiculos Militares')
	end)

	SetAllVehicleGeneratorsActive()
end)

